import UIKit

extension UIColor {
    static var contentGray: UIColor {
       return UIColor(named: "contentColor")!
    }
    
    static var darkBackground: UIColor {
        return UIColor(named: "darkBG")!
    }
    
    static var lightBackground: UIColor {
        return UIColor(named: "lightBG")!
    }
}
