import UIKit

extension UIFont {
    static var headerTitle: UIFont {
        return UIFont.boldSystemFont(ofSize: 20)
    }
    static var cellContent: UIFont {
        return UIFont.systemFont(ofSize: 13)
    }
    static var cellTitle: UIFont {
        return UIFont.boldSystemFont(ofSize: 15)
    }
}
